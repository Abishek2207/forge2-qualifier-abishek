def save_report(report_text: str) -> None:
    """
    Write the status report to outputs/autonomous-run.txt.

    Args:
        report_text: The full report string to write.
    """
    # Create the outputs/ directory if it does not already exist
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    with open(AUTO_RUN_FILE, "w", encoding="utf-8") as f:
        f.write(report_text)

    print(f"  ✅ Saved to: {AUTO_RUN_FILE}")


# ── Main Entry Point ──────────────────────────────────────────────────────────

def main():
    """
    Main function — runs the full autonomous status pipeline.

    Steps:
        1. Capture the run timestamp (UTC, ISO 8601)
        2. Build the structured status report
        3. Save it to outputs/autonomous-run.txt
        4. Print confirmation to the terminal
    """
